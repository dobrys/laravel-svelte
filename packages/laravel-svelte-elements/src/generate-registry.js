import { glob } from 'glob';
import { readFileSync, writeFileSync, mkdirSync } from 'fs';
import { relative, resolve, dirname } from 'path';

/**
 * Сканира componentsDir за .svelte файлове с `tag: "..."` в <svelte:options>
 * и записва registryOut като карта tag -> dynamic import().
 *
 * @param {object} options
 * @param {string} options.componentsDir
 * @param {string} options.registryOut
 * @param {string} [options.cwd]
 * @returns {{count: number}}
 */
export function generateRegistry({ componentsDir, registryOut, cwd = process.cwd() }) {
  const componentsAbs = resolve(cwd, componentsDir);
  const outAbs = resolve(cwd, registryOut);
  const outDir = dirname(outAbs);

  const svelteFiles = glob.sync('**/*.svelte', { cwd: componentsAbs });
  const entries = [];

  for (const file of svelteFiles) {
    const fullPath = resolve(componentsAbs, file);
    const content = readFileSync(fullPath, 'utf-8');
    const match = content.match(/tag:\s*["']([^"']+)["']/);
    if (!match) continue;

    const importPath = './' + relative(outDir, fullPath).replace(/\\/g, '/');
    entries.push(`  '${match[1]}': () => import('${importPath}')`);
  }

  mkdirSync(outDir, { recursive: true });
  writeFileSync(
    outAbs,
    `// AUTO-GENERATED by laravel-svelte-elements — do not edit manually\n` +
      `export const registry = {\n${entries.join(',\n')}\n};\n`
  );

  return { count: entries.length };
}
